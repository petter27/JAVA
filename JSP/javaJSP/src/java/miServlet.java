    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            out.println("<!DOCTYPE html>");
            out.println("<html>");
            out.println("<head>");
            out.println("<title>Calculadora</title>");            
            out.println("</head>");
            out.println("<body>");
            String numero1=request.getParameter("num1");
            String  numero2=request.getParameter("num2");
            String  op=request.getParameter("operacion");
            
            switch(op){
                case "suma":
                    int suma= Integer.parseInt(numero1)+Integer.parseInt(numero2);
                    out.println("La suma es: "+suma);
                    break;
                case "resta":
                    int resta= Integer.parseInt(numero1)-Integer.parseInt(numero2);
                    out.println("La resta es: "+resta);
                    break;
                case "multiplicacion":
                    int multi= Integer.parseInt(numero1)*Integer.parseInt(numero2);
                    out.println("La multiplicacion es: "+multi);
                    break;
                default:
                   out.println("No seleccion√≥ ninguna operacion"); 
                   break;
            }
            
            
            out.println("</body>");
            out.println("</html>");
        }
       
    }

